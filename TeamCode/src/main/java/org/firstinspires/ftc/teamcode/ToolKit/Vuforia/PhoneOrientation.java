public enum PhoneOrientation {
    /**
     * The phone is facing upright
     */
    UPRIGHT,
    /**
     * The phone is flipped upside down
     */
    UPSIDE_DOWN,
    /**
     * The phone is on its side, with the charger port
     * facing upwards
     */
    VOLUME_SIDE_DOWN,
    /**
     * The phone is on its side, with the volume buttons
     * facing upwards
     */
    VOLUME_SIDE_UP
}